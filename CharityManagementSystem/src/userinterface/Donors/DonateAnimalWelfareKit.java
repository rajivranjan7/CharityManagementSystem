    private void btnDonateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDonateActionPerformed
        // TODO add your handling code here:
         Organization organization = (Organization) comboBoxOrg.getSelectedItem();
         Organization.Type type = organization.getType();
         
        if (type == null) {
            comboBoxOrg.setBorder(BorderFactory.createLineBorder(Color.RED));
            comboBoxOrg.setForeground(Color.red);
        }
        
        if (txtQuantity.getText().isEmpty()) {
            txtQuantity.setBorder(BorderFactory.createLineBorder(Color.RED));
            txtQuantity.setForeground(Color.red);
        }
        
         if (txtQuantity.getText().isEmpty()
                || type == null )  {
            JOptionPane.showMessageDialog(null, "Kindly Provide all the Details!");
        } 
         else { 
             
             txtQuantity.setBorder(BorderFactory.createLineBorder(Color.black));
           
         AnimalWelfareKitInventoryRequest animalWelfareKitRequest = new AnimalWelfareKitInventoryRequest();
         animalWelfareKitRequest.setQuanity(Integer.parseInt(txtQuantity.getText()));
         animalWelfareKitRequest.setDonorName(account.getEmployee().getName());
         animalWelfareKitRequest.setDonorType(account.getRole().toString());
         animalWelfareKitRequest.setStatus("Donated");
         animalWelfareKitRequest.setRequestDate(new Date());
         animalWelfareKitRequest.setEnterprise(enterprise);
         animalWelfareKitRequest.setNetwork(network);
         animalWelfareKitRequest.setOrgType(type);
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                if(enterprise.getEnterpriseType() == Enterprise.EnterpriseType.InventoryManagementDirectory){
                    for(Organization org : enterprise.getOrganizationDirectory().getOrganizationList()){
                        if(org.getType() == Organization.Type.AnimalWelfareKitInventoryManagementOrganization){
                            if (org.getWorkQueue() == null) {
                            org.setWorkQueue(new WorkQueue());
                        }
                        org.getWorkQueue().getWorkRequestList().add(animalWelfareKitRequest);

                        }
                    }
                   
                }
            }
         JOptionPane.showMessageDialog(null, "Thank you for your donation!");
         txtQuantity.setText("");
         comboBoxOrg.setSelectedIndex(0);
         close();
         }
         
    }//GEN-LAST:event_btnDonateActionPerformed

